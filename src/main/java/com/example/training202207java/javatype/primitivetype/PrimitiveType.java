package com.example.training202207java.javatype.primitivetype;

/** プリミティブ型のプログラム. */
public class PrimitiveType {

  /** int型のサンプル. */
  public void setInt() {
    final int number = 1;

    System.out.println(number); // 1がコンソールに出力される
  }

  /** int型の比較. */
  public void compareInt() {
    final int number1 = 1;
    final int number2 = 1;

    System.out.println(number1 == number2); // trueがコンソールに出力される
  }
}
